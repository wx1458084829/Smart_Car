/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
	portBASE_TYPE pxHigherPriorityTaskWoken;
	
	if(__HAL_UART_GET_FLAG(&huart4, UART_FLAG_IDLE) == SET)
	{
			//测试代码
			printf("收到数据");
			__HAL_UART_CLEAR_FLAG(&huart4, UART_FLAG_IDLE);
			HAL_UART_DMAStop(&huart4);
		
			Uart4_Rcv.DataLen = 255 - hdma_uart4_rx.Instance->CNDTR;
		
			xQueueSendFromISR (Message_Queue, &Uart4_Rcv, &pxHigherPriorityTaskWoken);
		
			memset(&Uart4_Rcv, 0, sizeof(Uart4_Rcv));
	}
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */
	HAL_UART_Receive_DMA(&huart4, Uart4_Rcv.RcvBuf, 255);

  /* USER CODE END UART4_IRQn 1 */
}

